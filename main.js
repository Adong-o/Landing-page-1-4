var bookingButton = document.getElementById("button 1");

bookingButton.addEventListener("click", function() {
  var w = 600; // Set the width of the window
  var h = 400; // Set the height of the window
  var left = (screen.width/2) - (w/2); // Calculate the left position
  var top = (screen.height/2) - (h/2); // Calculate the top position
  window.open("booking.html", "Booking Page", "width=" + w + ",height=" + h + ",left=" + left + ",top=" + top);
});
